method = [
  [[4, 0], [4, 23], [4, 10], [6, 2], [6, 21], [6, 8], [12, 13], [12, 15], [12, 14]],
  [[5, 1], [5, 22], [5, 9], [7, 3], [7, 20], [7, 11], [16, 17], [16, 19], [16, 18]],
  [[7, 19], [7, 23], [7, 15], [6, 18], [6, 22], [6, 14], [8, 9], [8, 11], [8, 10]],
  [[4, 16], [4, 20], [4, 12], [5, 17], [5, 21], [5, 13], [2, 3], [2, 1], [2, 0]],
  [[4, 5], [4, 7], [4, 6], [2, 16], [2, 9], [2, 15], [3, 18], [3, 8], [3, 13]],
  [[20, 22], [20, 23], [20, 21], [0, 17], [0, 11], [0, 14], [1, 19], [1, 10], [1, 12]]
]

a = input().split()
b = a[:]

def check(a):
  for i in range(6):
    s = set([a.pop(), a.pop(), a.pop(), a.pop()])
    if len(s) > 1: return False
  return True

for i in method:
  a = b[:]
  for j in i:
    c, d = j
    a[c], a[d] = a[d], a[c]
  if check(a[:]): exit(print(1))

  a = b[:]
  for j in i[::-1]:
    c, d = j
    a[c], a[d] = a[d], a[c]
  if check(a[:]): exit(print(1))

print(0)
